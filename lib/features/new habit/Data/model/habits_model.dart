
import 'dart:ui';

class HabitsModel {
  String habitName;
  int habitColor;
  Image habitIcon;
  String frequency;
  String repeat;
  String time;
  String reminder;

  HabitsModel({
    required this.habitName ,
    required this.habitColor ,
    required this.habitIcon ,
    required this.frequency,
    required this.repeat ,
    required this.time ,
    required this.reminder});


}